public class MenuScreen implements Screen {

    private Neonpolis game;
    private SpriteBatch batch;
    private Stage stage;
    private Texture myTexture;
    private TextureRegion myTextureRegion;
    private TextureRegionDrawable myTexRegionDrawable;
    private ImageButton button;
    private Table table;
    FitViewport viewport;

    public MenuScreen (final Neonpolis game) {
        this.game = game;

        viewport = new FitViewport(200, 200, new OrthographicCamera());
        stage = new Stage(viewport);

        myTexture = new Texture("flatDark24.png");
        myTextureRegion = new TextureRegion(myTexture);
        myTexRegionDrawable = new TextureRegionDrawable(myTextureRegion);
        button = new ImageButton(myTexRegionDrawable);

        table = new Table();
        table.add(button).center().top();
        table.row();

        table.setFillParent(true);

        stage.addActor(table); //Add the button to the stage
        Gdx.input.setInputProcessor(stage);

        // menu button listeners
        button.addListener( new ClickListener() {
            @Override
            public void clicked(InputEvent event, float x, float y) {
                game.setScreen( new PlayScreen(game));
            }
        });
    }

    @Override
    public void show() {

    }

    @Override
    public void render(float delta) {
        Gdx.gl.glClearColor(1, 1, 1, 1);
        Gdx.gl.glClear(GL20.GL_COLOR_BUFFER_BIT);

        stage.act(Gdx.graphics.getDeltaTime());
        stage.draw();
    }

    @Override
    public void resize(int width, int height) {

    }

    @Override
    public void pause() {

    }

    @Override
    public void resume() {

    }

    @Override
    public void hide() {

    }

    @Override
    public void dispose() {

    }
}
